import os
import re
from bs4 import BeautifulSoup

# 1. папка с html файлами
folder_path = "pages_removed"

# список стоп-слов (предлоги, союзы и т.п.)
stopwords = {
    "и","в","во","не","что","он","на","я","с","со","как","а","то","все","она",
    "так","его","но","да","ты","к","у","же","вы","за","бы","по","только","ее",
    "мне","было","вот","от","меня","еще","нет","о","из","ему","теперь","когда",
    "даже","ну","вдруг","ли","если","уже","или","ни","быть","был","него","до",
    "вас","нибудь","опять","уж","вам","ведь","там","потом","себя","ничего",
    "ей","может","они","тут","где","есть","надо","ней","для","мы","тебя",
    "их","чем","была","сам","чтоб","без","будто","чего","раз","тоже","себе",
    "под","будет","ж","тогда","кто","этот","того","потому","этого","какой",
    "совсем","ним","здесь","этом","один","почти","мой","тем","чтобы","нее",
    "кажется","сейчас","были","куда","зачем","всех","никогда","можно","при",
    "наконец","два","об","другой","хоть","после","над","больше","тот","через",
    "эти","нас","про","всего","них","какая","много","разве","три","эту",
    "моя","впрочем","хорошо","свою","этой","перед","иногда","лучше","чуть",
    "том","нельзя","такой","им","более","всегда","конечно","всю","между"
}

tokens = set()

for filename in os.listdir(folder_path):
    if filename.endswith(".txt"):
        filepath = os.path.join(folder_path, filename)

        with open(filepath, "r", encoding="utf-8", errors="ignore") as f:
            html = f.read()

        # 2. извлечение текста из HTML
        soup = BeautifulSoup(html, "html.parser")
        text = soup.get_text(separator=" ")

        # 3. токенизация (только слова из русских букв)
        words = re.findall(r"[а-яё]+", text.lower())

        for word in words:
            if word not in stopwords and len(word) > 2:
                tokens.add(word)

# 4. сохранение
with open("tokens2.txt", "w", encoding="utf-8") as f:
    for token in sorted(tokens):
        f.write(token + "\n")

print(f"Сохранено {len(tokens)} уникальных токенов.")